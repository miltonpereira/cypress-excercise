/**
 * Command to login into the application
 */
const { login } = require("../pages/loginPage")

Cypress.Commands.add('login', () => {
    return cy.fixture('users').then((users) => {
        login.enterUsername(users.validUser["username"])
        login.enterPassword(users.validUser["password"])
        login.clickLoginButton()
    })
})
